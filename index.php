<p><span style="text-transform:none;">BIKESHARE IQ</span> is a web app that provides the current status of bikeshare stations in your city. It's an alternative to apps like Spotcycle. There's nothing to install, and it's lightweight and optimized for mobile.</p>
<p>It shows you each station's current supply of bikes and docks&mdash;as well as how the supply has changed in the past hour. is the number of bikes increasing or decreasing, and how quickly?</p>
<p>The default version is a map, but it also offers a text-only option that loads faster than maps do on phones. You can also isolate individual stations on their own pages, to load that station's information and nothing else. Bookmark the stations you use frequently to check their status fast.</p>
